export function Suma(num1: number, number2: number) {
  return num1 + number2;
}

export function Resta(num1: number, number2: number) {
    return num1 - number2;
}

export function Multiplica(num1: number, number2: number) {
    return num1 * number2;
}

export function Divide(num1: number, number2: number) {
    return num1 / number2;
}
